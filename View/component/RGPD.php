<h4>Article 15 - Protection des données personnelles</h4>

<p>
Données collectées :<br>

Les données à caractère personnel qui sont collectées sur ce site sont les suivantes :
</p>

<ul></ul>
    <li>ouverture de compte : lors de la création du compte de l'utilisateur, ses nom ; prénom ; adresse électronique ; n° de téléphone ;</li>
    <li>connexion : lors de la connexion de l'utilisateur au site web, celui-ci enregistre, notamment, ses nom, prénom, données de connexion, d'utilisation, de localisation et ses données relatives au paiement ;</li>
    <li>profil : l'utilisation des prestations prévues sur le site web permet de renseigner un profil, pouvant comprendre une adresse et un numéro de téléphone ;</li>
    <li>paiement : dans le cadre du paiement des produits et prestations proposés sur le site web, celui-ci enregistre des données financières relatives au compte bancaire ou à la carte de crédit de l'utilisateur ;</li>
    <li>communication : lorsque le site web est utilisé pour communiquer avec d'autres membres, les données concernant les communications de l'utilisateur font l'objet d'une conservation temporaire ;</li>
    <li>cookies : les cookies sont utilisés, dans le cadre de l'utilisation du site. L'utilisateur a la possibilité de désactiver les cookies à partir des paramètres de son navigateur.</li>
</ul>

<h4>Utilisation des données personnelles</h4>

<p>
Les données personnelles collectées auprès des utilisateurs ont pour objectif la mise à disposition des services du site web, leur amélioration et le maintien d'un environnement sécurisé. Plus précisément, les utilisations sont les suivantes :
</p>

<ul></ul>
    <li>accès et utilisation du site web par l'utilisateur ;</li>
    <li>gestion du fonctionnement et optimisation du site web ;</li>
    <li>organisation des conditions d'utilisation des Services de paiement ;</li>
    <li>vérification, identification et authentification des données transmises par l'utilisateur ;</li>
    <li>proposition à l'utilisateur de la possibilité de communiquer avec d'autres utilisateurs du site web ;</li>
    <li>mise en oeuvre d'une assistance utilisateurs ;</li>
    <li>personnalisation des services en affichant des publicités en fonction de l'historique de navigation de l'utilisateur, selon ses préférences ;</li>
    <li>prévention et détection des fraudes, malwares (malicious softwares ou logiciels malveillants) et gestion des incidents de sécurité ;</li>
    <li>gestion des éventuels litiges avec les utilisateurs ;</li>
    <li>envoi d'informations commerciales et publicitaires, en fonction des préférences de l'utilisateur.</li>
</ul>

<h4>Partage des données personnelles avec des tiers</h4>

<p>
Les données personnelles peuvent être partagées avec des sociétés tierces, dans les cas suivants :
</p>

<ul></ul>
    <li>quand l'utilisateur utilise les services de paiement, pour la mise en oeuvre de ces services, le site web est en relation avec des sociétés bancaires et financières tierces avec lesquelles elle a passé des contrats ;</li>
    <li>lorsque l'utilisateur publie, dans les zones de commentaires libres du site web, des informations accessibles au public ;</li>
    <li>quand l'utilisateur autorise le site web d'un tiers à accéder à ses données ;</li>
    <li>quand le site web recourt aux services de prestataires pour fournir l'assistance utilisateurs, la publicité et les services de paiement. Ces prestataires disposent d'un accès limité aux données de l'utilisateur, dans le cadre de l'exécution de ces prestations, et ont une obligation contractuelle de les utiliser en conformité avec les dispositions de la réglementation applicable en matière protection des données à caractère personnel ;</li>
    <li>si la loi l'exige, le site web peut effectuer la transmission de données pour donner suite aux réclamations présentées contre le site web et se conformer aux procédures administratives et judiciaires ;</li>
    <li>si le site web est impliquée dans une opération de fusion, acquisition, cession d'actifs ou procédure de redressement judiciaire, elle pourra être amenée à céder ou partager tout ou partie de ses actifs, y compris les données à caractère personnel. Dans ce cas, les utilisateurs seraient informés, avant que les données à caractère personnel ne soient transférées à une tierce partie.</li>
</ul>

<h4>Sécurité et confidentialité</h4>

<p>
Le site web met en oeuvre des mesures organisationnelles, techniques, logicielles et physiques en matière de sécurité du numérique pour protéger les données personnelles contre les altérations, destructions et accès non autorisés. Toutefois, il est à signaler qu'internet n'est pas un environnement complètement sécurisé et le site web ne peut pas garantir la sécurité de la transmission ou du stockage des informations sur internet.
</p>

<h4>Mise en oeuvre des droits des utilisateurs</h4>

<p>
En application de la réglementation applicable aux données à caractère personnel, les utilisateurs disposent des droits ci-dessous mentionnés, qu'ils peuvent exercer en faisant leur demande à l'adresse suivante : contact.culinair@gmail.com.<br>

Le droit d’accès : ils peuvent exercer leur droit d'accès, pour connaître les données personnelles les concernant. Dans ce cas, avant la mise en œuvre de ce droit, le site web peut demander une preuve de l'identité de l'utilisateur afin d'en vérifier l'exactitude.
Le droit de rectification : si les données à caractère personnel détenues par le site web sont inexactes, ils peuvent demander la mise à jour des informations.
Le droit de suppression des données : les utilisateurs peuvent demander la suppression de leurs données à caractère personnel, conformément aux lois applicables en matière de protection des données.
Le droit à la limitation du traitement : les utilisateurs peuvent de demander au site web de limiter le traitement des données personnelles conformément aux hypothèses prévues par le RGPD.
Le droit de s’opposer au traitement des données : les utilisateurs peuvent s’opposer à ce que ses données soient traitées conformément aux hypothèses prévues par le RGPD.
Le droit à la portabilité : ils peuvent réclamer que le site web leur remette les données personnelles qui lui sont fournies pour les transmettre à un nouveau site web.
Evolution de la présente clause.<br>

Le site web se réserve le droit d'apporter toute modification à la présente clause relative à la protection des données à caractère personnel à tout moment. Si une modification est apportée à la présente clause de protection des données à caractère personnel, le site web s'engage à publier la nouvelle version sur son site. Le site web informera également les utilisateurs de la modification par messagerie électronique, dans un délai minimum de 15 jours avant la date d'effet. Si l'utilisateur n'est pas d'accord avec les termes de la nouvelle rédaction de la clause de protection des données à caractère personnel, il a la possibilité de supprimer son compte.<br>
</p>
